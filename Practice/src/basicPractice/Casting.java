package basicPractice;

public class Casting {
	
	
	
	
	

	public static void main(String[] args) {
		
	//--------implicit casting-------------------------------
		
		int a = 20;
		double b;
		
		b = a;
		
		System.out.println(b);
		
	
		
		//--------explicit casting-------------------------------
	
		double c = 20.3345;
		
		int d = (int) c;
		
		System.out.println(d);
		
		
		
	}
}
